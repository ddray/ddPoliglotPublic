class WordService {
  static Future<List<Word>> getRecomended() async {
    final extractedData = await HttpUtils.get('Word/GetRecomended1');
    var items = (extractedData["data"] as List<dynamic>)
        .map((e) => Word.fromJson(e as Map<String, dynamic>))
        .toList();
    return items;
  }

  static Future<Map<String, dynamic>> getRecomendedCandidates() async {
    final extractedData = await HttpUtils.get('Word/GetRecomendedCandidates1');
    var words = (extractedData["words"] as List<dynamic>).map((e) {
      var w = Word.fromJson(e as Map<String, dynamic>);
      return w;
    }).toList();

    var repetition = (extractedData["repetition"] as List<dynamic>).map((e) {
      var w = Word.fromJson(e as Map<String, dynamic>);
      return w;
    }).toList();
    var lastLessonNum = extractedData['lastUserLessonNum'] as int;
    return {
      'words': words,
      'lastUserLessonNum': lastLessonNum,
      'repetition': repetition
    };
  }

  static getFillRecomended(
      List<Word> selectedWords, List<Word> wordsForRepetition) async {
    final wordIDs = selectedWords.map((e) => e.wordID).toList();
    final repeatWordIds = wordsForRepetition.map((e) => e.wordID).toList();
    final body = json.encode({
      'wordIDs': wordIDs,
      'repeatWordIds': repeatWordIds,
    });

    final extractedData = await HttpUtils.post('Word/GetFillRecomended1', body);

    var words = (extractedData["words"] as List<dynamic>).map((e) {
      var w = Word.fromJson(e as Map<String, dynamic>);
      return w;
    }).toList();

    var repetition =
        (extractedData["wordsForRepetition"] as List<dynamic>).map((e) {
      var w = Word.fromJson(e as Map<String, dynamic>);
      return w;
    }).toList();

    var userLesson = UserLesson.fromJson(extractedData["userLesson"]);

    return {'userLesson': userLesson, 'words': words, 'repetition': repetition};
  }

  static Future<DictionaryResponse> getDictionary(curVersion) async {
    final extractedData =
        await HttpUtils.get('Word/GetDictionary?version=$curVersion');
    var serverVersion = extractedData["version"];
    var dictWords = (extractedData["dictWords"] as List<dynamic>)
        .map((e) => DictWord.fromJson(e as Map<String, dynamic>))
        .toList();
    var userLessons = (extractedData["userLessons"] as List<dynamic>)
        .map((e) => UserLesson.fromJson(e as Map<String, dynamic>))
        .toList();
    return DictionaryResponse(serverVersion, dictWords, userLessons);
  }

